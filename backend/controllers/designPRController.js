// GET PR BY ID
// =====================================================
exports.getById = async (req, res) => {
    try {
        const { prId } = req.params;

        const [prs] = await db.query(`
            SELECT pr.*, 
                   p.project_code, p.project_name,
                   u1.full_name AS created_by_name,
                   u2.full_name AS submitted_by_name,
                   u3.full_name AS approved_by_name
            FROM design_purchase_requests pr
            LEFT JOIN projects p ON pr.project_id = p.id
            LEFT JOIN users u1 ON pr.created_by = u1.id
            LEFT JOIN users u2 ON pr.submitted_by = u2.id
            LEFT JOIN users u3 ON pr.approved_by = u3.id
            WHERE pr.id = ?
        `, [prId]);

        if (prs.length === 0) {
            return res.status(404).json({ success: false, message: 'Không tìm thấy PR' });
        }

        const pr = prs[0];

        // Get PR lines
        const [lines] = await db.query(`
            SELECT pl.*, bl.source_unit_id, du.unit_code AS source_unit_code
            FROM design_pr_lines pl
            LEFT JOIN design_bom_lines bl ON pl.bom_line_id = bl.id
            LEFT JOIN design_units du ON bl.source_unit_id = du.id
            WHERE pl.pr_id = ?
            ORDER BY pl.material_code
        `, [prId]);

        // Group by vendor
        const byVendor = lines.reduce((acc, line) => {
            const vendorId = line.vendor_id || 'no_vendor';
            if (!acc[vendorId]) {
                acc[vendorId] = [];
            }
            acc[vendorId].push(line);
            return acc;
        }, {});

        res.json({
            success: true,
            data: {
                ...pr,
                lines,
                lines_by_vendor: byVendor,
                total_lines: lines.length
            }
        });
    } catch (error) {
        console.error('Error getting PR:', error);
        res.status(500).json({ success: false, message: error.message });
    }
};

