// APPROVE PR (Submitted → Approved)
// =====================================================
exports.approve = async (req, res) => {
    const requestId = uuidv4();
    let connection;

    try {
        const { prId } = req.params;
        const user = req.user;

        connection = await db.getConnection();
        await connection.beginTransaction();

        // Get PR
        const [prs] = await connection.query('SELECT * FROM design_purchase_requests WHERE id = ?', [prId]);

        if (prs.length === 0) {
            throw new Error('Không tìm thấy PR');
        }

        const pr = prs[0];

        if (pr.status !== 'submitted') {
            throw new Error(`Chỉ có thể duyệt PR đã submitted. Trạng thái hiện tại: ${pr.status}`);
        }

        // Update PR
        await connection.query(`
            UPDATE design_purchase_requests 
            SET status = 'approved', approved_by = ?, approved_at = NOW(), row_version = row_version + 1
            WHERE id = ?
        `, [user.id, prId]);

        // Create inventory reservations
        const [prLines] = await connection.query('SELECT * FROM design_pr_lines WHERE pr_id = ?', [prId]);

        for (const line of prLines) {
            await connection.query(`
                INSERT INTO design_inventory_reservations 
                (project_id, design_revision_id, bom_id, material_id, qty_reserved, reserved_by, reserved_at)
                VALUES (?, ?, ?, ?, ?, ?, NOW())
            `, [
                pr.project_id,
                pr.design_revision_id,
                pr.bom_id,
                line.material_id,
                line.qty,
                user.id
            ]);
        }

        // Audit log
        await connection.query(`
            INSERT INTO design_audit_logs 
            (request_id, entity_type, entity_id, action, new_values, user_id, user_name, created_at)
            VALUES (?, 'pr', ?, 'approved', ?, ?, ?, NOW())
        `, [
            requestId, prId,
            JSON.stringify({ reservations_created: prLines.length }),
            user.id, user.full_name
        ]);

        await connection.commit();

        res.json({
            success: true,
            message: `Đã duyệt ${pr.pr_code}. Đã tạo ${prLines.length} reservation.`
        });
    } catch (error) {
        if (connection) await connection.rollback();
        console.error('Error approving PR:', error);
        res.status(500).json({ success: false, message: error.message });
    } finally {
        if (connection) connection.release();
    }
};

