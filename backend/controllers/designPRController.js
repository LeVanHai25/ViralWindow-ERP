// SUBMIT PR (Draft → Submitted)
// =====================================================
exports.submit = async (req, res) => {
    const requestId = uuidv4();
    let connection;

    try {
        const { prId } = req.params;
        const user = req.user;

        connection = await db.getConnection();
        await connection.beginTransaction();

        // Get PR
        const [prs] = await connection.query('SELECT * FROM design_purchase_requests WHERE id = ?', [prId]);

        if (prs.length === 0) {
            throw new Error('Không tìm thấy PR');
        }

        const pr = prs[0];

        if (pr.status !== 'draft') {
            throw new Error(`Chỉ có thể submit PR ở trạng thái Draft. Trạng thái hiện tại: ${pr.status}`);
        }

        // Update PR
        await connection.query(`
            UPDATE design_purchase_requests 
            SET status = 'submitted', submitted_by = ?, submitted_at = NOW(), row_version = row_version + 1
            WHERE id = ?
        `, [user.id, prId]);

        // Audit log
        await connection.query(`
            INSERT INTO design_audit_logs 
            (request_id, entity_type, entity_id, action, user_id, user_name, created_at)
            VALUES (?, 'pr', ?, 'submitted', ?, ?, NOW())
        `, [requestId, prId, user.id, user.full_name]);

        await connection.commit();

        res.json({
            success: true,
            message: `Đã gửi ${pr.pr_code} để duyệt`
        });
    } catch (error) {
        if (connection) await connection.rollback();
        console.error('Error submitting PR:', error);
        res.status(500).json({ success: false, message: error.message });
    } finally {
        if (connection) connection.release();
    }
};

