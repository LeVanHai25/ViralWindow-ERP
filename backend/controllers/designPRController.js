// REJECT PR
// =====================================================
exports.reject = async (req, res) => {
    const requestId = uuidv4();
    let connection;

    try {
        const { prId } = req.params;
        const { reason } = req.body;
        const user = req.user;

        if (!reason) {
            return res.status(400).json({ success: false, message: 'Vui lòng nhập lý do từ chối' });
        }

        connection = await db.getConnection();
        await connection.beginTransaction();

        // Get PR
        const [prs] = await connection.query('SELECT * FROM design_purchase_requests WHERE id = ?', [prId]);

        if (prs.length === 0) {
            throw new Error('Không tìm thấy PR');
        }

        const pr = prs[0];

        if (pr.status !== 'submitted') {
            throw new Error(`Chỉ có thể từ chối PR đã submitted. Trạng thái hiện tại: ${pr.status}`);
        }

        // Update PR
        await connection.query(`
            UPDATE design_purchase_requests 
            SET status = 'draft', rejected_by = ?, rejected_at = NOW(), rejection_reason = ?, row_version = row_version + 1
            WHERE id = ?
        `, [user.id, reason, prId]);

        // Audit log
        await connection.query(`
            INSERT INTO design_audit_logs 
            (request_id, entity_type, entity_id, action, reason, user_id, user_name, created_at)
            VALUES (?, 'pr', ?, 'rejected', ?, ?, ?, NOW())
        `, [requestId, prId, reason, user.id, user.full_name]);

        await connection.commit();

        res.json({
            success: true,
            message: `Đã từ chối ${pr.pr_code}. PR chuyển về Draft để chỉnh sửa.`
        });
    } catch (error) {
        if (connection) await connection.rollback();
        console.error('Error rejecting PR:', error);
        res.status(500).json({ success: false, message: error.message });
    } finally {
        if (connection) connection.release();
    }
};

