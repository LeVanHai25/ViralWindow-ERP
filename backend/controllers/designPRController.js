// REFRESH INVENTORY (for PR lines)
// =====================================================
exports.refreshInventory = async (req, res) => {
    let connection;

    try {
        const { prId } = req.params;

        connection = await db.getConnection();

        // Get PR lines
        const [lines] = await connection.query(
            'SELECT * FROM design_pr_lines WHERE pr_id = ?',
            [prId]
        );

        for (const line of lines) {
            const inventoryData = await getInventoryStatus(line.material_code, connection);

            await connection.query(`
                UPDATE design_pr_lines 
                SET qty_on_hand = ?, qty_reserved = ?, qty_available = ?, qty_on_order = ?,
                    qty_shortage = GREATEST(0, qty - ?),
                    snapshot_at = NOW()
                WHERE id = ?
            `, [
                inventoryData.qty_on_hand,
                inventoryData.qty_reserved,
                inventoryData.qty_available,
                inventoryData.qty_on_order,
                inventoryData.qty_available,
                line.id
            ]);
        }

        res.json({
            success: true,
            message: `Đã refresh tồn kho cho ${lines.length} dòng`
        });
    } catch (error) {
        console.error('Error refreshing inventory:', error);
        res.status(500).json({ success: false, message: error.message });
    } finally {
        if (connection) connection.release();
    }
};
